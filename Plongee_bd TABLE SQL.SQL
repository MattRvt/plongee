CREATE TABLE PLO_PERSONNE
 (
   PER_NUM INT NOT NULL  AUTO_INCREMENT,
   PER_NOM VARCHAR(30) NULL,
   PER_PRENOM VARCHAR(30) NULL, 
   PRIMARY KEY (PER_NUM) 
 );

