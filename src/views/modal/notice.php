            <p><b>Comment accéder aux plongées affichées sur la page d'acceuil ? </b></p>
            Les plongées affichées sur la page d'accueil sont accessibles depuis l'onglet plongée.
            <p></p
            <p><b>Pourquoi toutes les plongées, les sites, les embarcations, les aptitudes ou les personnes ne sont pas
                    supprimables ?</b></p>
            Une plongée ne peut être supprimé que si la plongée n'est pas complète.<br>
            Les sites, les embarcations, les aptitudes et les personnes ne peuvent être supprimés que s'ils ne font pas
            partie d'une plongée ou que personne ne possède l'aptitude à supprimer.
            <p></p>
            <p><b>Pourquoi je ne peux pas ajouter de palanquer à une plongée ?</b></p>
            Pour ajouter des palanquées à une plongée, il faut que celle-ci ne soit pas passée. Il est impossible d'ajouter des palanquées à une plongée qui a déjà eu lieu.
